/* 1)Exercício: Declarando Variáveis
Uma variável é um espaço de memória para armazenar valores. Podemos criá-los e, por sua vez, também podemos atribuir-lhes um certo valor.

Em JavaScript, podemos declará-los usando a palavra var  e depois o nome que queremos dar.
Sua tarefa será declarar duas variáveis: uma com o nome idade  e o outro com o nome peso.

    var idade;
    var peso; */

/*2) Exercício: Atribuindo valores a variáveis
Já vimos como declarar uma variável! Mas como podemos armazenar valores dentro dela?

Para armazenar um valor dentro de uma variável, iremos utilizar um símbolo =   seguido do valor que queremos armazenar. Em programação, 
chamamos esse procedimento de atribuição. Exemplo: var meses = 12;
var quantidadeDeAlunos = 30;

Declare duas variáveis chamadas  idade  e  peso,  e atribua um valor numérico a ambas.*/ 

var idade = 30;
var peso = 80;

