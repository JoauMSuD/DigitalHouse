/* 4) As constantes
Agora pense nesse exemplo, temos que validar algumas pessoas pelas suas idades. 
Nosso critério será 18 anos como idade miníma e 99 como idade máxima. Ninguém pode mudar essas definições. 
Para que isso ocorra de fato iremos escrever constantes. Vale dizer que, por questões de boas práticas,  
é comum utilizar nome com letras maiúsculas na hora de definir uma constante.

Bom agora, você deve criar um constante chamada IDADE_MINIMA, e outra chamada IDADE_MAXIMA. Que por sua vez, devem ter os valores 18 e 99 respectivamente.

*/
